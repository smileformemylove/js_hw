'use strict';

const firstNumber = parseFloat(prompt("Введіть перше число:"));
const secondNumber = parseFloat(prompt("Введіть друге число:"));

console.log(`Результат: ${firstNumber} + ${secondNumber} =`, firstNumber + secondNumber)
console.log(`Результат: ${firstNumber} - ${secondNumber} =`, firstNumber - secondNumber)
console.log(`Результат: ${firstNumber} * ${secondNumber} =`, firstNumber * secondNumber)
console.log(`Результат: ${firstNumber} / ${secondNumber} =`, firstNumber / secondNumber)


